nome = "Victor Ferreira"

# Fatiamento para obter o primeiro nome
primeiro_nome = nome[:6]
print(primeiro_nome)  # Saída: Victor

# Fatiamento para obter o sobrenome
sobrenome = nome[7:]
print(sobrenome)  # Saída: Ferreira

# Fatiamento para obter apenas as iniciais
iniciais = nome[0] + nome[7]
print(iniciais)  # Saída: VF

# Fatiamento invertido para obter o nome ao contrário
nome_invertido = nome[::-1]
print(nome_invertido)  # Saída: arreireF rotciV
